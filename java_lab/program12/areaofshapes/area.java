package areaofshapes;

public class area {

    public double circle(int r)
    {
        return 3.14*r*r;
    }

    public double triangle(int b,int h)
    {
        return .5*b*h ;
    }

    public int rectangle(int l,int b)
    {
        return l*b;
    }

    public int square(int a)
    {
            return a*a;
    }

}
